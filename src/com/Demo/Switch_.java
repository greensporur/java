package com.Demo;

public class Switch_ {

	
	public static void main(String[] args) {
		
		String i = "java ";
		
		switch(i) {
		
		case "java":
			System.out.println("Java");
			break;
		case "Selenium":
			System.out.println("Selenium");
			break;
		case "Project":
			System.out.println("Project");
		default:
			System.out.println("Enter valid value");
		
		
		}
		
	}
}
